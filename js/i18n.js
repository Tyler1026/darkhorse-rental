function setLang(lang){
  if(!I18N[lang]) lang="ja";
  document.documentElement.lang = lang==="zh"?"zh-CN":lang;
  document.querySelectorAll("[data-i18n]").forEach(function(el){
    var key=el.getAttribute("data-i18n"), v=I18N[lang][key];
    if(v==null){ if(window.console) console.warn("[i18n] missing key:",key,"for",lang); return; }
    if(v.indexOf("|")>-1){
      var p=v.split("|");
      el.innerHTML = p[0]+"<span>"+(p[1]||"")+"</span>"+(p[2]||"");
    } else el.textContent=v;
  });
  document.querySelectorAll(".lang a").forEach(function(a){
    a.classList.toggle("active", a.getAttribute("data-lang")===lang);
  });
  // <title> と meta description をページ別に設定
  var page=document.body.getAttribute("data-page");
  if(page){
    var t=I18N[lang]["t_"+page], d=I18N[lang]["d_"+page];
    if(t) document.title=t;
    if(d){
      var m=document.querySelector('meta[name="description"]');
      if(!m){ m=document.createElement("meta"); m.setAttribute("name","description"); document.head.appendChild(m); }
      m.setAttribute("content",d);
    }
  }
  try{ localStorage.setItem("dh_lang", lang); }catch(e){}
}
document.addEventListener("DOMContentLoaded",function(){
  var saved="ja"; try{ saved=localStorage.getItem("dh_lang")||"ja"; }catch(e){}
  document.querySelectorAll(".lang a").forEach(function(a){
    a.addEventListener("click",function(e){e.preventDefault();setLang(a.getAttribute("data-lang"));});
  });
  setLang(saved);
});